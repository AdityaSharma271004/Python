# break -> break the loop right now where it find the condition

'''
marks = [23,56,12,90,45]
for  nums in marks:
    
    if(nums == 12):
        break
    print(nums)'''


# continue -> it skip the value which was given in the condition and loop executed    

'''
marks = [23,56,12,90,45]
for  nums in marks:
    
    if(nums == 12):
        continue
    print(nums)'''


# pass -> it allows you to run another condition without ending for loop    

marks = [23,56,12,90,45]
for  nums in marks:
    pass


